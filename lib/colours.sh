NONE()
{
  echo -e -n '\033[00m'
}
RED()
{
  echo -e -n '\033[01;31m'
}
GREEN()
{
  echo -e -n '\033[01;32m'
}
YELLOW()
{
  echo -e -n '\033[01;33m'
}
PURPLE()
{
  echo -e -n '\033[01;35m'
}
CYAN()
{
  echo -e -n '\033[01;36m'
}
WHITE()
{
  echo -e -n '\033[01;37m'
}
BOLD()
{
  echo -e -n '\033[1m'
}
UNDERLINE()
{
  echo -e -n '\033[4m'
}
